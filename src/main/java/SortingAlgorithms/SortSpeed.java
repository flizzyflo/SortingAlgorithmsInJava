package main.java.SortingAlgorithms;


/**
 * The SortSpeed class defines constants for various sorting algorithms
 * and their associated speeds.
 *
 * This class provides a convenient way to access the speeds of different
 * sorting algorithms without hardcoding the specific values in various
 * parts of the code.
 **/
public class SortSpeed {

    final static public int MERGE_SORT = 2;
    final static public int BUBBLE_SORT = 20;
    final static public int HEAP_SORT = 30;
    final static public int QUICK_SORT = 45;
    final static public int RADIX_SORT = 300;
    final static public int SELECTION_SORT = 50;

}
